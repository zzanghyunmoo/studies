namespace Output;

class Program
{
    static void Main(string[] args)
    {
        Console.WriteLine(30);              // int
        Console.WriteLine(30.1);            // double
        Console.WriteLine(30.2f);           // float
        Console.WriteLine(true);            // bool
        Console.WriteLine('p');             // char
        Console.WriteLine("Hello World!");  // string
    }
}
